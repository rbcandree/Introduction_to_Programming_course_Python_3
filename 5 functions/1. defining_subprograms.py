"""
Build three subprograms using the code lines provided in this order:
add(a,b) which calculates and returns the sum of a and b
multiply(a,b) which calculates and returns the product of a and b
subtract(a,b) which calculates and returns the result of a - b
"""

def add(a,b):
    c = a + b
    return c

def multiply(a,b):
    d = a * b
    return d

def subtract(a,b):
    return a - b
print(add(5, 3))
print(multiply(5, 3))
print(subtract(5, 3))